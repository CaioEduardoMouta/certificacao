package br.com.alura.teste;

public class TestaSwitch {
    public static void main(String[] args) {
        int opcao = 3;
        switch (opcao) {
            case 1:
            System.out.println("Primeira Opção");
            break;
            case 2:
            System.out.println("Segunda Opção");
            break;
            case 3:
            System.out.println("Terceira Opção");
            default:
        }
    }
}
